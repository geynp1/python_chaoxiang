a = 3 + 4
print(a)

a = 4 / 2
print(a)

a = 4//2
print(a)

a = 5//2
print(a)

# 获取余数
a = 5%2
print(a)

#幂运算
a = 5**2
print(a)

# 增强赋值操作符
a+=3  # a = a + 3
a/=3
a//=3
a%=3
a**=3
print(a)

# 整数和浮点数混合运算，输出结果是浮点数
a  = 4 //2.0
print(a)

#整数之间运算，产生结果类型与操作符相关，/运算的结果是浮点数；
a = 4/2
print(a)

#  整数或浮点数与复数运算，输出结果是复数。
a = 10-(1+1j)
print(a)

a = abs(-4)
print(a)

# 5整除3的结果，5整除3的余数
a = divmod(5,3)
print(a)

# pow取幂 3的4次方取2的余数
a = pow(3,4,2)
print(a)

# round函数取近似值
a = round(3.1425926)
print(a)
a = round(3.1425926,4)
print(a)

# max
a = max(3,4,6,654,34,534,5,4534,5,34,5345)
print(a)

# min
a = min(3,4,6,654,34,534,5,4534,5,34,5345)
print(a)